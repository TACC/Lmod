require("strict")
require("pairsByKeys")
local BeautifulTbl = require ('BeautifulTbl')


Bare         = inheritsFrom(BaseShell)
Bare.my_name = 'bare'

function Bare.expand(self,tbl)
   local t = {}
   for k in pairsByKeys(tbl) do
      local v = tbl[k]
      t[#t + 1] = {k, '"'..v..'"'}
   end

   local bt = BeautifulTbl:new(t)
   io.stdout:write(bt:build_tbl(),"\n")
end

return Bare
